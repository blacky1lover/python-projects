"""
  namma kudikura value or list or tuple or etc...
in  ithu kulla irukka nu check panna
    irunthuchu na True nu varum
    illana false nu varum
not in   ithu kulla illa na True nu varum irukkuna False nu varum

"""
# in
a=[1,2,3,4]
print(4 in a)
print(8 in a)
# not in
a=[1,2,3,4]
print(4 not in a)
print(8 not in a)
